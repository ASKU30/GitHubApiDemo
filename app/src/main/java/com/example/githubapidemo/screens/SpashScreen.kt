import androidx.navigation.NavController
import com.example.githubapidemo.R
import com.example.githubapidemo.navigation.NavigationItem
import kotlinx.coroutines.delay

@Composable
fun SplashScreen(navController: NavController) {
    val scale = remember {
        androidx.compose.animation.core.Animatable(0f)
    }
    Surface(
        modifier = Modifier
            .fillMaxSize()
    ) {
        LaunchedEffect(key1 = true) {
            scale.animateTo(
                targetValue = 0.7f,
                animationSpec = tween(
                    durationMillis = 800,
                    easing = {
                        OvershootInterpolator(4f).getInterpolation(it)
                    }
                )
            )
            delay(1000L)
            navController.navigate(route = NavigationItem.HomeScreen.route) {
                popUpTo(NavigationItem.Splash.route) { inclusive = true }
                launchSingleTop = true
            }
        }
        ShowLogo()
    }
}

@Preview(showBackground = true)
@Composable
fun ShowLogo() {
    Box(
        modifier = Modifier.fillMaxSize().background(
            color = Color(0x0FFFFFFF)
        ),
        contentAlignment = Alignment.Center
    ) {
        Image(
            modifier = Modifier.width(300.dp).height(300.dp),
            painter = painterResource(R.drawable.logo),
            contentDescription = "Logo"
        )
    }
}
